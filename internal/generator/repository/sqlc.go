// SqlcStrategy генерирует слой репозитория через инструментарий sqlc.
// Поддерживает два режима:
//   - generate: codegen записывает sqlc.yaml + .sql файлы, затем вызывает `sqlc generate`.
//   - existing: codegen читает sqlc.yaml пользователя для получения RepositoryContract
//     и генерирует только слои service + handler поверх него.
type SqlcStrategy struct{}

func (SqlcStrategy) Name() string { return "sqlc" }

func (s SqlcStrategy) Prepare(ctx context.Context, sc *schema.Schema, opts Options) error {
	if sc.Repository.Sqlc == nil || sc.Repository.Sqlc.Mode != "generate" {
		return nil
	}
	return s.runSqlcGenerate(ctx, sc, opts)
}

func (SqlcStrategy) Contract(s *schema.Schema) (*RepositoryContract, error) {
	d := strings.Title(strings.ToLower(s.Domain))
	return &RepositoryContract{
		InterfaceName: d + "Repository",
		MethodName:    operationToMethod(s.Repository.Operation),
		InputType:     d + "Request",
		OutputType:    d + "Response",
	}, nil
}

func (s SqlcStrategy) Files(sc *schema.Schema, opts Options) ([]generator.File, error) {
	if sc.Repository.Sqlc == nil {
		return nil, fmt.Errorf("sqlc strategy: missing sqlc configuration")
	}

	switch sc.Repository.Sqlc.Mode {
	case "generate":
		return s.generateFiles(sc, opts)
	case "existing":
		// В режиме existing codegen не записывает файлы репозитория.
		// Слои service и handler генерируются поверх типов sqlc пользователя.
		return nil, nil
	default:
		return nil, fmt.Errorf("sqlc strategy: unknown mode %q", sc.Repository.Sqlc.Mode)
	}
}

// generateFiles создаёт конфигурацию sqlc и файл SQL-запросов, которые будут
// использоваться командой `sqlc generate` (вызывается в Prepare).
func (SqlcStrategy) generateFiles(s *schema.Schema, opts Options) ([]generator.File, error) {
	domain := strings.ToLower(s.Domain)
	baseDir := strings.TrimRight(opts.OutputDir, "/")
	sqlcDir := filepath.Join(baseDir, "gen", "sqlc")

	sqlcYAML, err := buildSqlcYAML(s, domain, baseDir)
	if err != nil {
		return nil, err
	}
	sqlQuery, err := buildSQLQuery(s)
	if err != nil {
		return nil, err
	}

	return []generator.File{
		{
			Path:      filepath.Join(sqlcDir, "sqlc.gen.yaml"),
			Content:   []byte(sqlcYAML),
			Protected: true,
		},
		{
			Path:      filepath.Join(sqlcDir, "queries.gen.sql"),
			Content:   []byte(sqlQuery),
			Protected: true,
		},
	}, nil
}

const sqlcYAMLTmpl = `# Code generated by codegen DO NOT EDIT.
version: "2"
sql:
  - engine: "postgresql"
    queries: "./queries.gen.sql"
    schema: "."
    gen:
      go:
        package: "db"
        out: "../../db"
        emit_json_tags: true
        emit_prepared_queries: false
`

func buildSqlcYAML(_ *schema.Schema, _, _ string) (string, error) {
	return sqlcYAMLTmpl, nil
}

const sqlQueryTmpl = `-- Code generated by codegen DO NOT EDIT.
-- source: {{.SourceFile}}
{{ $op := .Operation -}}
{{ if eq $op "insert" -}}
-- name: {{.MethodName}} :one
INSERT INTO {{.Schema}}.{{.Table}} ({{.Columns}})
VALUES ({{.Placeholders}})
RETURNING {{.ReturningCols}};
{{ else if eq $op "select" -}}
-- name: {{.MethodName}} :one
SELECT {{.SelectCols}}
FROM {{.Schema}}.{{.Table}}
WHERE {{.WhereClause}};
{{ else if eq $op "update" -}}
-- name: {{.MethodName}} :exec
UPDATE {{.Schema}}.{{.Table}}
SET {{.SetClause}}
WHERE {{.WhereClause}};
{{ else if eq $op "delete" -}}
-- name: {{.MethodName}} :exec
DELETE FROM {{.Schema}}.{{.Table}}
WHERE {{.WhereClause}};
{{ end }}`

type sqlQueryData struct {
	SourceFile    string
	MethodName    string
	Operation     string
	Schema        string
	Table         string
	Columns       string
	Placeholders  string
	ReturningCols string
	SelectCols    string
	WhereClause   string
	SetClause     string
}

func buildSQLQuery(s *schema.Schema) (string, error) {
	cols := s.Repository.Fields
	ph := make([]string, len(cols))
	for i := range cols {
		ph[i] = fmt.Sprintf("$%d", i+1)
	}
	outCols := make([]string, len(s.Output))
	for i, f := range s.Output {
		outCols[i] = f.Name
	}
	whereParts := make([]string, len(cols))
	for i, c := range cols {
		whereParts[i] = fmt.Sprintf("%s = $%d", c, i+1)
	}
	setParts := make([]string, len(cols))
	for i, c := range cols {
		setParts[i] = fmt.Sprintf("%s = $%d", c, i+1)
	}

	data := sqlQueryData{
		SourceFile:    "system-gen.yaml",
		MethodName:    operationToMethod(s.Repository.Operation),
		Operation:     strings.ToLower(s.Repository.Operation),
		Schema:        s.Repository.Schema,
		Table:         s.Repository.Table,
		Columns:       strings.Join(cols, ", "),
		Placeholders:  strings.Join(ph, ", "),
		ReturningCols: strings.Join(outCols, ", "),
		SelectCols:    strings.Join(outCols, ", "),
		WhereClause:   strings.Join(whereParts, " AND "),
		SetClause:     strings.Join(setParts, ", "),
	}

	tmpl, err := template.New("sql").Parse(sqlQueryTmpl)
	if err != nil {
		return "", fmt.Errorf("sqlc strategy: parse sql template: %w", err)
	}
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("sqlc strategy: execute sql template: %w", err)
	}
	return buf.String(), nil
}

// runSqlcGenerate записывает сгенерированные файлы на диск и вызывает `sqlc generate`.
func (s SqlcStrategy) runSqlcGenerate(ctx context.Context, sc *schema.Schema, opts Options) error {
	files, err := s.generateFiles(sc, opts)
	if err != nil {
		return err
	}

	// Записываем файлы перед запуском sqlc
	for _, f := range files {
		if err := os.MkdirAll(filepath.Dir(f.Path), 0o755); err != nil {
			return fmt.Errorf("sqlc strategy: mkdir %s: %w", filepath.Dir(f.Path), err)
		}
		if err := os.WriteFile(f.Path, f.Content, 0o644); err != nil {
			return fmt.Errorf("sqlc strategy: write %s: %w", f.Path, err)
		}
	}

	// Определяем каталог, содержащий sqlc.gen.yaml
	baseDir := strings.TrimRight(opts.OutputDir, "/")
	sqlcDir := filepath.Join(baseDir, "gen", "sqlc")

	cmd := exec.CommandContext(ctx, "sqlc", "generate")
	cmd.Dir = sqlcDir
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr

	if err := cmd.Run(); err != nil {
		return fmt.Errorf("sqlc strategy: sqlc generate failed: %w\n(is sqlc installed? run: go install github.com/sqlc-dev/sqlc/cmd/sqlc@latest)", err)
	}
	return nil
}
